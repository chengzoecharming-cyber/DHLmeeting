# 省会城市：小圆点 + 文字标签
CAPITALS = [
    ("广州", 23.1291, 113.2644),
    ("长沙", 28.2280, 112.9388),
    ("武汉", 30.5928, 114.3055),
    ("海口", 20.0440, 110.1999),
]


def build_heatmap() -> None:
    # 使用高德公开瓦片，配色接近截图
    m = folium.Map(
        location=[25.0, 112.5],
        zoom_start=6,
        tiles=None,
    )

    # 高德标准地图瓦片（无 Key，公开可访问）
    folium.TileLayer(
        tiles="https://webrd0{s}.is.autonavi.com/appmaptile?lang=zh_cn&size=1&scale=1&style=8&x={x}&y={y}&z={z}",
        attr="高德地图",
        name="高德地图",
        subdomains=["1", "2", "3", "4"],
        max_zoom=18,
        min_zoom=1,
        overlay=False,
        control=False,
    ).add_to(m)

    # 热力图：高德默认彩虹色带，与截图完全一致
    # 低值 -> 高值：深蓝紫 -> 蓝 -> 青 -> 绿 -> 黄 -> 红
    HeatMap(
        data=DATA,
        radius=55,
        blur=40,
        max_zoom=6,
        min_opacity=0.2,
        gradient={
            0.0: "rgba(0,0,180,0.2)",      # 深蓝紫
            0.2: "rgba(0,120,220,0.5)",    # 蓝
            0.4: "rgba(0,220,220,0.65)",   # 青
            0.6: "rgba(60,220,60,0.75)",   # 绿
            0.8: "rgba(255,220,0,0.9)",    # 黄
            1.0: "rgba(255,40,0,1.0)",     # 红核心
        },
    ).add_to(m)

    # 省会城市标记：小圆点 + 文字标签
    for name, lat, lng in CAPITALS:
        folium.CircleMarker(
            location=[lat, lng],
            radius=5,
            color="#333333",
            fill=True,
            fill_color="#ff4444",
            fill_opacity=0.9,
            weight=1,
        ).add_to(m)
        folium.map.Marker(
            location=[lat, lng],
            icon=folium.DivIcon(
                icon_size=(80, 20),
                icon_anchor=(40, -10),
                html=f'<div style="font-size:13px;color:#2a2a2a;text-align:center;white-space:nowrap;font-weight:bold;text-shadow:1px 1px 0 #fff,-1px -1px 0 #fff,1px -1px 0 #fff,-1px 1px 0 #fff;">{name}</div>',
            ),
        ).add_to(m)

    out = Path("coverage_heatmap.html")
    m.save(out)
    print(f"已保存: {out.resolve()}")
    print("用浏览器打开，F11 全屏后截图即可放入 PPT。")


if __name__ == "__main__":
    build_heatmap()
